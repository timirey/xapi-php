<?php

namespace Timirey\XApi\Enums;

/**
 * Enum representing the day of the week.
 */
enum Day: int
{
    /*
     * Monday.
     */
    case MONDAY = 1;

    /*
     * Tuesday.
     */
    case TUESDAY = 2;

    /*
     * Wednesday.
     */
    case WEDNESDAY = 3;

    /*
     * Thursday.
     */
    case THURSDAY = 4;

    /*
     * Friday.
     */
    case FRIDAY = 5;

    /*
     * Saturday.
     */
    case SATURDAY = 6;

    /*
     * Sunday.
     */
    case SUNDAY = 7;
}
